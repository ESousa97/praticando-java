package io.github.enoquesousa.praticandojava.examples;

public class OperadoresLogicosOR {
  public static void main(String[] args) {
    boolean a = true;
    boolean b = false;

    if (a || b) {
      System.out.println("Este é um exemplo do operador OR já que a é " + a + ", e b é " + b);
    }
  }
}
